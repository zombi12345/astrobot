def get_daily_seed():
    """Генерирует уникальный seed для каждого дня, чтобы гороскоп не повторялся"""
    today = datetime.now().strftime('%Y-%m-%d')
    return int(hashlib.md5(today.encode()).hexdigest()[:8], 16)

